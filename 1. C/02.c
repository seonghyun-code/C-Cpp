#include <stdio.h> // 프로그램에 <stdio.h>이라는 파일을 포함해라.

// () : 함수 ... 명령어를 실행시켜주는 애
int main() { // main 함수 : 가장 먼저 실행되는 함수
	printf("Hello, World!\nHello, World!\n"); // ; : 마침표 역할, printf : 출력 함수, \n : 엔터

	// 예제
	printf("동해물과 백두산이\n마르고\n닳도록\n");
	printf("동해물과 백두산이\n");
	printf("마르고\n");
	printf("닳도록\n");
	return 0;
}

#include <stdio.h>
/* 6. 코드만 보고 답 유추 */

int main() {
	int arr[3][3] = { 0 };
	/*
	[0][0][0] 100 104 108
	[0][0][0] 112 116 120
	[0][0][0] 124 128 132 ... 136 140 ...
	*/
	printf("%d\n", &arr[0][0]); // 100
	printf("%d\n", arr[0] + 1); // 한 칸 : &arr[0][0] + 4 * 1 = 104
	printf("%d\n", &arr[0] + 1); // 한 줄 : &arr[0] + 4 * 3 = 112
	printf("%d\n", arr + 1); // 한 줄 : arr = arr + 0 = &arr[0],  112
	printf("%d\n", &arr + 1); // 전체 : &arr + 4 * 9 = 136
}